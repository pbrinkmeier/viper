/**
 * Enumeration of possible zoom types. This contains the actions of zooming in
 * and zooming out.
 */
public enum ZoomType {
    /**
     * Zoom into the visualisation
     */
    ZOOM_IN,

    /**
     * Zoom out of the visualisation
     */
    ZOOM_OUT
}